let color_semaforo = "verde";

if (color_semaforo === "rojo") {
    console.log("Espera");
} else if (color_semaforo === "amarillo") {
    console.log("Precaución");
} else if (color_semaforo === "verde") {
    console.log("Cruzar");
} else {
    console.log("Color de semáforo no válido");
}